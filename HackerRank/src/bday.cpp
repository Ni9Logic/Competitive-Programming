#include <bits/stdc++.h>
using namespace std;

void solve()
{
    long long b, w;
    cin >> b >> w;
    long long bc, wc, z;
    cin >> bc >> wc >> z;

    printf("b --> %lld\n", b);
    printf("w --> %lld\n", w);
    printf("bc --> %lld\n", bc);
    printf("wc --> %lld\n", wc);
    printf("z --> %lld\n", z);
}

int main()
{
    int n;
    cin >> n;
    while (n--)
        solve();
}